# variables in python
# variables are used to store data in it
a = 5  # int
b = 5.3  # float
c = "Variables are used to store data"  # string
d = True  # boolean

# Assigning multiple values
x, y, z = 19, "Vamstar", False
print(x)
print(y)
print(z)

# To get the data type of variable we use
print(a, type(a))
print(b, type(b))
print(c, type(c))
print(d, type(d))

